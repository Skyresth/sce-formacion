package org.gobcan.sce.apps.form.formacion.data.dto;

public class DetalleCentrosDto {
    private String nif;
    private String razonSocial;
    private String nombreComercial;
    private String domicilioNotificacion;
    private String telefono;
    private String fax;
    private String correo;
    private String numeroInscripcion;
    private String codmuni;

    // Getters and Setters

    public String getNif() {
        return nif;
    }

    public void setNif(String nif) {
        this.nif = nif;
    }

    public String getRazonSocial() {
        return razonSocial;
    }

    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }

    public String getNombreComercial() {
        return nombreComercial;
    }

    public void setNombreComercial(String nombreComercial) {
        this.nombreComercial = nombreComercial;
    }

    public String getDomicilioNotificacion() {
        return domicilioNotificacion;
    }

    public void setDomicilioNotificacion(String domicilioNotificacion) {
        this.domicilioNotificacion = domicilioNotificacion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getNumeroInscripcion() {
        return numeroInscripcion;
    }

    public void setNumeroInscripcion(String numeroInscripcion) {
        this.numeroInscripcion = numeroInscripcion;
    }

    public String getCodmuni() {
        return codmuni;
    }

    public void setCodmuni(String codmuni) {
        this.codmuni = codmuni;
    }
}